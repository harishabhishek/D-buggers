$(document).ready(function(){

/* --------------------------------------------------------------------------------------------------------------------------------------------------
CONVERT NAV TO SELECT ELEMENT
-------------------------------------------------------------------------------------------------------------------------------------------------- */

	$("<select />").appendTo("nav");
	$("<option />", {
		"selected": "selected",
		"value"   : "",
		"text"    : "Choose category..."
	}).appendTo("nav select");
	$("nav a").each(function() {
		var el = $(this);
		$("<option />", {
		"value"   : el.attr("href"),
		"text"    : el.text()
		}).appendTo("nav select");
	});
	$("nav select").change(function() {
		window.location = $(this).find("option:selected").val();
	});

/* --------------------------------------------------------------------------------------------------------------------------------------------------
END
-------------------------------------------------------------------------------------------------------------------------------------------------- */

});
